    public void issue_table() throws SQLException{
         
         st = con.createStatement();
         rs = st.executeQuery("select * from issue");
         
         ResultSetMetaData rsm = rs.getMetaData();
         int c;
         c = rsm.getColumnCount();
         
         DefaultTableModel df = (DefaultTableModel)jTable2.getModel();
         df.setRowCount(0);
         
         while(rs.next()){
             Vector v = new Vector();
             
             for(int i = 1;i<=c;i++){
             v.add(rs.getString("id"));
             v.add(rs.getString("book_name"));
             v.add(rs.getString("issuer_name"));
             v.add(rs.getString("contact"));
             v.add(rs.getString("price"));
             v.add(rs.getString("category"));
             v.add(rs.getString("issue_date"));
             v.add(rs.getString("return_date"));
             
             
         }
             df.addRow(v);
     }
     }


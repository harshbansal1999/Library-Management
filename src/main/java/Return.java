    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        SimpleDateFormat formatter = new SimpleDateFormat("dd-MMM-yyyy");
        
        String returned = rrd.getText();
        String return_date = rreturn.getText();
        
        
        try {
            Date returned_ = (Date) formatter.parse(returned);
            Date return_ = (Date) formatter.parse(return_date);
            
            String x =formatter.format(returned_);
            String y =formatter.format(return_);
            
            
            if(x.compareTo(y)>0){
                rfine.setText("100");
            }
            else{
                rfine.setText("No fine");
            }
            
            
            
        } catch (ParseException ex) {
            Logger.getLogger(Return.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


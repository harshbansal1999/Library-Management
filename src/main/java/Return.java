    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        
        DefaultTableModel d1 = (DefaultTableModel)jTable2.getModel();
        int selectIndex = jTable2.getSelectedRow();
        
        rid.setText(d1.getValueAt(selectIndex,0).toString());
        rname.setText(d1.getValueAt(selectIndex,1).toString());
        rissuer.setText(d1.getValueAt(selectIndex,2).toString());
        rcontact.setText(d1.getValueAt(selectIndex,3).toString());
        //rrd.setText(d1.getValueAt(selectIndex,6).toString());
        rreturn.setText(d1.getValueAt(selectIndex,7).toString());
        
        String returned = rrd.getText();
        String return_date = rreturn.getText();
        
        
        try {
            Date returned_ = (Date) new SimpleDateFormat("dd/MM/yyyy").parse(returned);
            Date return_ = (Date) new SimpleDateFormat("dd/MM/yyyy").parse(return_date);
            
            if(returned_.compareTo(return_)>0){
                rfine.setText("100");
            }
            else{
                rfine.setText("No fine");
            }
            
            
            
        } catch (ParseException ex) {
            Logger.getLogger(Return.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jTable2MouseClicked

